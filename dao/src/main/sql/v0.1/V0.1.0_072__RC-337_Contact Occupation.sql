DECLARE
		V_SYS_PROP_EXIST VARCHAR2(5);
    V_SYS_PROP_ID NUMBER;
    V_SORT_ORDER NUMBER := 1;
    ----------------------------
      CURSOR C1 IS SELECT * FROM DBO_TC.SYS_PROP_RC
		WHERE SYS_PROPERTY='CONTACT_OCCUPATION';
		
		CURSOR C2 IS 
		SELECT * FROM 
			DBO_TC.SYS_PROP_VALUE 
		WHERE 
			SYS_PROP_ID=(SELECT SYS_PROP_ID FROM DBO_TC.SYS_PROPERTY WHERE SYS_PROP_NAME='CONTACT_OCCUPATION')
		ORDER BY PROP_VALUE ASC;
    ------------------------------
		FUNCTION SYS_PROP_VALUE_FNC(P_SYS_PROP_VALUE IN VARCHAR2,P_SYS_PROP_NAME IN VARCHAR2)
    RETURN VARCHAR2
		AS
		V_SYS_PROP_EXIST VARCHAR2(1);
		BEGIN
		SELECT 'Y'
		INTO V_SYS_PROP_EXIST
		FROM DBO_TC.SYS_PROPERTY SP,
		DBO_TC.SYS_PROP_VALUE SPV
		WHERE 
		SP.SYS_PROP_ID=SPV.SYS_PROP_ID
		AND UPPER(SPV.PROP_VALUE)=TRIM(UPPER(P_SYS_PROP_VALUE))
		AND UPPER(SP.SYS_PROP_NAME)=TRIM(UPPER(P_SYS_PROP_NAME));
		RETURN V_SYS_PROP_EXIST;
		EXCEPTION
			WHEN OTHERS THEN
				V_SYS_PROP_EXIST:='N';
				RETURN V_SYS_PROP_EXIST;
		END SYS_PROP_VALUE_FNC;
		--===============================
		PROCEDURE  SYS_PROP_VALUE_INS(P_SYS_PROP_VALUE IN VARCHAR2,P_SYS_PROP_ID IN NUMBER)
		AS
		BEGIN
		INSERT INTO DBO_TC.SYS_PROP_VALUE (PROP_ID,SYS_PROP_ID,PROP_VALUE,ABBR,SORT_ORDER) 
		VALUES (DBO_TC.SEQ_SYS_PROP_VALUE_ID.NEXTVAL,P_SYS_PROP_ID,P_SYS_PROP_VALUE,P_SYS_PROP_VALUE,null);
		EXCEPTION
			WHEN OTHERS THEN
				NULL;
		END SYS_PROP_VALUE_INS;
		--=============================================

    BEGIN
		--
		BEGIN
		  SELECT SP.SYS_PROP_ID
		  INTO V_SYS_PROP_ID
		  from DBO_TC.SYS_PROPERTY SP
		, DBO_TC.SYS_PROP_VALUE SPN
		WHERE SYS_PROP_NAME='CONTACT_OCCUPATION'
		AND SPN.SYS_PROP_ID=SP.SYS_PROP_ID
		GROUP BY SP.SYS_PROP_ID;
		  EXCEPTION
			WHEN OTHERS  THEN 
			  DBMS_OUTPUT.PUT_LINE('V_SYS_PROP_ID for does not exist CONTACT_OCCUPATION');
			END;
			--
		IF V_SYS_PROP_ID IS NOT NULL THEN 
		--
		FOR I IN C1 
		LOOP
			BEGIN
			V_SYS_PROP_EXIST:='N';
			V_SYS_PROP_EXIST:=SYS_PROP_VALUE_FNC(I.SYS_PROP_VALUE,I.SYS_PROPERTY);
			IF V_SYS_PROP_EXIST='N' THEN 
				SYS_PROP_VALUE_INS(I.SYS_PROP_VALUE,V_SYS_PROP_ID);
			END IF;
			END;
		END LOOP;
		COMMIT;
		
		FOR J IN C2
		LOOP
		BEGIN
			DBMS_OUTPUT.PUT_LINE  ('UPDATING PROP_ID: ' || J.PROP_ID || ' SORT ORDER AS ' || V_SORT_ORDER);
			UPDATE DBO_TC.SYS_PROP_VALUE SET SORT_ORDER = V_SORT_ORDER WHERE PROP_ID = J.PROP_ID;
			V_SORT_ORDER := V_SORT_ORDER + 1;
		END;
		END LOOP;
		COMMIT;
			--
		END IF;
	END;
